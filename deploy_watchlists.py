def main():
    conn = sqlite3.connect(DB_PATH, timeout=30.0)
    c = conn.cursor()
    for market, tickers in WATCHLISTS.items():
        c.execute("""
            INSERT INTO watchlists (market, tickers, updated_at)
            VALUES (?, ?, datetime('now'))
            ON CONFLICT(market) DO UPDATE SET
                tickers = excluded.tickers,
                updated_at = excluded.updated_at
        """, (market, json.dumps(tickers)))
    conn.commit()
    # Verify
    c.execute("SELECT market, length(tickers), updated_at FROM watchlists ORDER BY market")
    print(f"{'market':15s} {'bytes':>7s}  updated_at")
    print("-" * 55)
    for row in c.fetchall():
        print(f"{row[0]:15s} {row[1]:>7d}  {row[2]}")
    conn.close()
    print()
    print("✅ All 6 watchlists deployed. Now run: systemctl restart alphabot")

if __name__ == "__main__":
    main()
